void solve(){
    ll n, x; cin >> n >> x;
    ll sum = 0;
    for(ll i = 1; i <= x; i++){
        ll cut_j = min((n / i), (x - i));
        for(ll j = 1; j <= cut_j; j++){
            ll cut1 = (n - (i * j))/(i + j);
            ll cut2 = (x - i - j);
            ll mini = min(cut1, cut2);
            if(mini <= 0) continue;
            sum += mini;
        }
    }
    cout << sum << endl;
}
